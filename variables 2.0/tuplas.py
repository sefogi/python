# la ventaja de las tuplas es que guardan un solo espacio en memoria a diferencia de las listas, es inmutable
#para leer datos que no se van a modificar
# creando tuplas con tuple
tupla = tuple(["dato1","dato2"])

# creando un tupla sin parentesis debe de estar separados por una coma (,)
tupla_1 = "dato1","dato2"

# creando una tupla sin parentesis de un solo dato "para reconocer que es una tupla siempre al final cuando se ingresa un solo dato debe tener una ,"
tupla_2 = "dato3",
